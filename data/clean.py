def match_num_lines(src_files, tgt_files):
    '''
    检查每个文件里 src 和 tgt 行数是否一致
    '''
    assert len(src_files) == len(tgt_files)
    num_files = len(src_files)
    print(f'Number of files: {num_files}')

    src_samples = []
    tgt_samples = []
    for i in tqdm(range(num_files), leave=False):
        with open(src_files[i]) as src_file, open(tgt_files[i]) as tgt_file:
            src_sents = src_file.read().splitlines()
            tgt_sents = tgt_file.read().splitlines()
            assert len(src_sents) == len(tgt_sents), f'{i}-th 文件 src 和 tgt 行数不一致'
            src_samples.extend(src_sents)
            tgt_samples.extend(tgt_sents)

    return src_samples, tgt_samples


def deduplicate(src_samples, tgt_samples):
    '''
    去除 src 和 tgt 完全一样的句子对
    '''
    assert len(src_samples) == len(tgt_samples)
    num_samples = len(src_samples)
    print(f'Number of samples before deduplicate: {num_samples}')

    num_duplicates = 0
    src_samples_cleaned = []
    tgt_samples_cleaned = []
    for i in tqdm(range(num_samples), leave=False):
        if src_samples[i] == tgt_samples[i]:
            num_duplicates += 1
        else:
            src_samples_cleaned.append(src_samples[i])
            tgt_samples_cleaned.append(tgt_samples[i])

    print(f'Number of samples after deduplicate: {num_samples-num_duplicates}')

    return src_samples_cleaned, tgt_samples_cleaned


def handle_special_chars(samples):
    '''
    处理特殊字符
    '''
    removed = []
    for sent in tqdm(samples, leave=False):
        # 修正引号使用
        sent = sent.replace('“', '"')
        sent = sent.replace('”', '"')
        sent = re.sub(r'\"(.+?)\"', r'“\1”', sent)  # 双引号
        sent = sent.replace('‘', '\'')
        sent = sent.replace('’', '\'')
        sent = re.sub(r'\'(.+?)\'', r'‘\1’', sent)  # 单引号
        # 替换中英文相似符号
        sent = sent.replace('!', '！')
        sent = sent.replace(':', '：')
        # 移除省略号
        sent = re.sub(r'\.*', '', sent)
        # 移除特殊符号
        for char in SPECIALS:
            sent = sent.replace(char, '')
        # 移除空括号
        sent = sent.replace('（）', '')
        # 移除格式例如：（出《xxx》）
        sent = re.sub(r'（出《(.+?)》）', '', sent)
        # 加到数组
        removed.append(sent)
    return removed


def main():
    src_files = glob.glob('data/双语数据/**/src.txt', recursive=True)
    tgt_files = glob.glob('data/双语数据/**/tgt.txt', recursive=True)

    src_samples, tgt_samples = match_num_lines(src_files, tgt_files)
    src_samples, tgt_samples = deduplicate(src_samples, tgt_samples)
    src_samples = handle_special_chars(src_samples)
    tgt_samples = handle_special_chars(tgt_samples)

    with open('data/src.txt', 'w') as f:
        f.writelines([s + '\n' for s in src_samples])
    with open('data/tgt.txt', 'w') as f:
        f.writelines([t + '\n' for t in tgt_samples])


if __name__ == '__main__':
    main()
